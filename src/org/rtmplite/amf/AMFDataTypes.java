package org.rtmplite.amf;

public class AMFDataTypes {
	public static final byte NUMBER = 0x00; // Long type
	public static final byte BOOLEAN = 0x01; // Boolean type
	public static final byte STRING = 0x02; // String type
	public static final byte OBJECT = 0x03; // Object type
	public static final byte NULL = 0x05; // Null type
}
